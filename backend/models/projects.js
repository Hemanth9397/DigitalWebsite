import mongoose from "mongoose";

const projectSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    techStack: [String],
    image: String,
  },
  { timestamps: true }
);

export default mongoose.model("Project", projectSchema);
